    private void botao_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_entrarActionPerformed
        usuario = campo_usuario.getText();
        senha = campo_senha.getText();
        if(botao_mysql.isSelected())
        {
            url = "jdbc:mysql://localhost/";
        }
        if(botao_postgre.isSelected())
        {
            url = "jdbc:postgresql://localhost/";
        }
        
        try {
            FileWriter arq = new FileWriter("./init.txt");
            if(check_lembrar.isSelected() == false)
            {
                arq.write("0");
                arq.close();
            }
            else
            {
                arq.write("1\n");
                arq.write(usuario + "\n");
                arq.write(senha + "\n");
                arq.write(url + "\n");
                arq.close();
            }
                
        } catch (IOException ex) {
            System.out.println("Erro: " + ex);
        }
        tela_principal principal = new tela_principal(usuario, senha, url);
        principal.show();
        dispose();
        
    }//GEN-LAST:event_botao_entrarActionPerformed

